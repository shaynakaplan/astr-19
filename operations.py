#comparison operators
x = 9
y = 3
print("x == y ?", x == y)		#True if x equals y. False otherwise
print("x != y ?", x != y)		#True is x doesn't equal y. False otherwise
print("x > y ?", x > y)			#True if x is greater than y. False otherwise
print("x < y ?", x < y)			#True if x is less than y. False otherwise
print("x >= y ?", x >= y)		#True if x is greater than or equal to y. False otherwise
print("x <= y ?", x <= y)		#True if x is less than or equal to y. False otherwise